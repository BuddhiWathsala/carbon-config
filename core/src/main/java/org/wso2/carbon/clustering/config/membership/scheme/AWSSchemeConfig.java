package org.wso2.carbon.clustering.config.membership.scheme;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlElementWrapper;
import java.util.ArrayList;
import java.util.List;

@XmlAccessorType(XmlAccessType.FIELD)
public class AWSSchemeConfig {

    @XmlElement(name = "AccessKey")
    private String accessKey;

    @XmlElement(name = "SecretKey")
    private String secretKey;

    @XmlElement(name = "SecurityGroup")
    private String securityGroup;

    @XmlElement(name = "Region", defaultValue = "us-east-1")
    private String region;

    @XmlElement(name = "TagKey")
    private String tagKey;

    @XmlElement(name = "TagValue")
    private String tagValue;

    @XmlElement(name = "ConnTimeout")
    private int connTimeout;

    @XmlElement(name = "HostHeader")
    private String hostHeader;

    public String getAccessKey() {
        return accessKey;
    }

    public void setAccessKey(String accessKey) {
        this.accessKey = accessKey;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public String getSecretKey() {
        return secretKey;
    }

    public void setSecretKey(String secretKey) {
        this.secretKey = secretKey;
    }

    public String getSecurityGroup() {
        return securityGroup;
    }

    public void setSecurityGroup(String securityGroup) {
        this.securityGroup = securityGroup;
    }

    public String getTagKey() {
        return tagKey;
    }

    public void setTagKey(String tagKey) {
        this.tagKey = tagKey;
    }

    public String getTagValue() {
        return tagValue;
    }

    public void setTagValue(String tagValue) {
        this.tagValue = tagValue;
    }

    public int getConnTimeout() {
        return connTimeout;
    }

    public void setConnTimeout(int connTimeout) {
        this.connTimeout = connTimeout;
    }

    public String getHostHeader() {
        return hostHeader;
    }

    public void setHostHeader(String hostHeader) {
        this.hostHeader = hostHeader;
    }

    public String toString() {
        return "AWS";
    }
}
